<template>
  <Dialog :title="dialogTitle" v-model="dialogVisible">
    <el-form
      ref="formRef"
      :model="formData"
      :rules="formRules"
      label-width="128px"
      v-loading="formLoading"
    >
      <el-form-item label="车编号" prop="carNo">
        <el-input v-model="formData.carNo" placeholder="请输入车编号" />
      </el-form-item>
      <el-form-item label="车辆类别：0-电车，1-油车" prop="type">
        <el-radio-group v-model="formData.type">
          <el-radio value="1">请选择字典生成</el-radio>
        </el-radio-group>
      </el-form-item>
      <el-form-item label="车牌号码" prop="licensePlateNumber">
        <el-input v-model="formData.licensePlateNumber" placeholder="请输入车牌号码" />
      </el-form-item>
      <el-form-item label="使用状态: 0-闲置, 1-被使用, 2-被占用申请" prop="useStatus">
        <el-radio-group v-model="formData.useStatus">
          <el-radio value="1">请选择字典生成</el-radio>
        </el-radio-group>
      </el-form-item>
    </el-form>
    <template #footer>
      <el-button @click="submitForm" type="primary" :disabled="formLoading">确 定</el-button>
      <el-button @click="dialogVisible = false">取 消</el-button>
    </template>
  </Dialog>
</template>
<script setup lang="ts">
import { CarApi, CarVO } from '@/api/business/returnCar/car'

/** 公司车辆 表单 */
defineOptions({ name: 'CarForm' })

const { t } = useI18n() // 国际化
const message = useMessage() // 消息弹窗

const dialogVisible = ref(false) // 弹窗的是否展示
const dialogTitle = ref('') // 弹窗的标题
const formLoading = ref(false) // 表单的加载中：1）修改时的数据加载；2）提交的按钮禁用
const formType = ref('') // 表单的类型：create - 新增；update - 修改
const formData = ref({
  id: undefined,
  carNo: undefined,
  type: undefined,
  licensePlateNumber: undefined,
  useStatus: undefined,
})
const formRules = reactive({
  carNo: [{ required: true, message: '车编号不能为空', trigger: 'blur' }],
  type: [{ required: true, message: '车辆类别：0-电车，1-油车不能为空', trigger: 'blur' }],
  licensePlateNumber: [{ required: true, message: '车牌号码不能为空', trigger: 'blur' }],
})
const formRef = ref() // 表单 Ref

/** 打开弹窗 */
const open = async (type: string, id?: number) => {
  dialogVisible.value = true
  dialogTitle.value = t('action.' + type)
  formType.value = type
  resetForm()
  // 修改时，设置数据
  if (id) {
    formLoading.value = true
    try {
      formData.value = await CarApi.getCar(id)
    } finally {
      formLoading.value = false
    }
  }
}
defineExpose({ open }) // 提供 open 方法，用于打开弹窗

/** 提交表单 */
const emit = defineEmits(['success']) // 定义 success 事件，用于操作成功后的回调
const submitForm = async () => {
  // 校验表单
  await formRef.value.validate()
  // 提交请求
  formLoading.value = true
  try {
    const data = formData.value as unknown as CarVO
    if (formType.value === 'create') {
      await CarApi.createCar(data)
      message.success(t('common.createSuccess'))
    } else {
      await CarApi.updateCar(data)
      message.success(t('common.updateSuccess'))
    }
    dialogVisible.value = false
    // 发送操作成功的事件
    emit('success')
  } finally {
    formLoading.value = false
  }
}

/** 重置表单 */
const resetForm = () => {
  formData.value = {
    id: undefined,
    carNo: undefined,
    type: undefined,
    licensePlateNumber: undefined,
    useStatus: undefined,
  }
  formRef.value?.resetFields()
}
</script>
