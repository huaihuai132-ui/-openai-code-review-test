<template>
  <Dialog :title="dialogTitle" v-model="dialogVisible">
    <el-form
      ref="formRef"
      :model="formData"
      :rules="formRules"
      label-width="128px"
      v-loading="formLoading"
    >
      <el-form-item label="承租户名称" prop="renterName">
        <el-input v-model="formData.renterName" placeholder="请输入承租户名称" />
      </el-form-item>
      <el-form-item label="承租户联系电话" prop="renterPhone">
        <el-input v-model="formData.renterPhone" placeholder="请输入承租户联系电话" />
      </el-form-item>
      <el-form-item label="承租户其他联系方式" prop="renterContact">
        <el-input v-model="formData.renterContact" placeholder="请输入承租户其他联系方式" />
      </el-form-item>
      <el-form-item label="承租户身份证号" prop="renterIdCard">
        <el-input v-model="formData.renterIdCard" placeholder="请输入承租户身份证号" />
      </el-form-item>
      <el-form-item label="承租户联系地址" prop="renterContactAddress">
        <el-input v-model="formData.renterContactAddress" placeholder="请输入承租户联系地址" />
      </el-form-item>
      <el-form-item label="备注信息" prop="remarks">
        <el-input v-model="formData.remarks" placeholder="请输入备注信息" />
      </el-form-item>
    </el-form>
    <template #footer>
      <el-button @click="submitForm" type="primary" :disabled="formLoading">确 定</el-button>
      <el-button @click="dialogVisible = false">取 消</el-button>
    </template>
  </Dialog>
</template>
<script setup lang="ts">
import { AssetRenterApi, AssetRenterVO } from '@/api/business/asset/assetRenter'

/** 资产承租人信息 表单 */
defineOptions({ name: 'AssetRenterForm' })

const { t } = useI18n() // 国际化
const message = useMessage() // 消息弹窗

const dialogVisible = ref(false) // 弹窗的是否展示
const dialogTitle = ref('') // 弹窗的标题
const formLoading = ref(false) // 表单的加载中：1）修改时的数据加载；2）提交的按钮禁用
const formType = ref('') // 表单的类型：create - 新增；update - 修改
const formData = ref({
  id: undefined,
  renterName: undefined,
  renterPhone: undefined,
  renterContact: undefined,
  renterIdCard: undefined,
  renterContactAddress: undefined,
  remarks: undefined,
})
const formRules = reactive({
  renterName: [{ required: true, message: '承租户名称不能为空', trigger: 'blur' }],
})
const formRef = ref() // 表单 Ref

/** 打开弹窗 */
const open = async (type: string, id?: number) => {
  dialogVisible.value = true
  dialogTitle.value = t('action.' + type)
  formType.value = type
  resetForm()
  // 修改时，设置数据
  if (id) {
    formLoading.value = true
    try {
      formData.value = await AssetRenterApi.getAssetRenter(id)
    } finally {
      formLoading.value = false
    }
  }
}
defineExpose({ open }) // 提供 open 方法，用于打开弹窗

/** 提交表单 */
const emit = defineEmits(['success']) // 定义 success 事件，用于操作成功后的回调
const submitForm = async () => {
  // 校验表单
  await formRef.value.validate()
  // 提交请求
  formLoading.value = true
  try {
    const data = formData.value as unknown as AssetRenterVO
    if (formType.value === 'create') {
      await AssetRenterApi.createAssetRenter(data)
      message.success(t('common.createSuccess'))
    } else {
      await AssetRenterApi.updateAssetRenter(data)
      message.success(t('common.updateSuccess'))
    }
    dialogVisible.value = false
    // 发送操作成功的事件
    emit('success')
  } finally {
    formLoading.value = false
  }
}

/** 重置表单 */
const resetForm = () => {
  formData.value = {
    id: undefined,
    renterName: undefined,
    renterPhone: undefined,
    renterContact: undefined,
    renterIdCard: undefined,
    renterContactAddress: undefined,
    remarks: undefined,
  }
  formRef.value?.resetFields()
}
</script>
